# =============================================================================
# Faça um programa em Python que receba quatro notas,
# calcule e imprima a média aritmética. Observe o exemplo abaixo
# =============================================================================

# Entrada dos valores para cálculo da média
x1 = float(input('Digite a primeira nota: '))
x2 = float(input('Digite a segunda nota: '))
x3 = float(input('Digite a terceira nota: '))
x4 = float(input('Digite a quarta nota: '))

media = (x1+x2+x3+x4) / 4

print('\nA média aritmética é ', media)
